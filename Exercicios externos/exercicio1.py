"""
Crie um programa em Python que utilize a estrutura
while para contar de 1 até 10. A cada número, o programa 
deve exibir o valor atual na tela. Ao final, exiba a 
mensagem: "Contagem finalizada!".
"""

contador = 0

while contador <= 10:
    print(contador)
    contador += 1

print('Contagem finalizada!')
