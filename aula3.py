"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Daniel Jesus')
print(1, 'Daniel "Nascimento"')

# Aspas duplas
print("Daniel Nascimento")
print(2, "Daniel 'Nascimento'")

# Escape
print("Daniel \"Nascimento\"")
# r
print(r"Daniel \"Nascimento\"")
